function isLang(value: string | null): value is Lang {
  return value === 'es' || value === 'en';
}

function isTheme(value: string | null): value is Theme {
  return Boolean(value && value in THEMES);
}

// ─── Context ──────────────────────────────────────────────────────────────────
interface AppContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: TranslationKeys;
  theme: Theme;
  setTheme: (th: Theme) => void;
  tc: ThemeColors;
  activeTab: string;
  setActiveTab: (tab: string) => void;
  selectedArtistName: string;
  setSelectedArtistName: (name: string) => void;
  selectedAlbumKey: string;
  setSelectedAlbumKey: (key: string) => void;
  selectedTrackKey: string;
  setSelectedTrackKey: (key: string) => void;
  topSubTab: 'artists' | 'albums' | 'tracks';
  setTopSubTab: (sub: 'artists' | 'albums' | 'tracks') => void;
}

const AppContext = createContext<AppContextValue>({
  lang: 'es',
  setLang: () => {},
  t: STRINGS.es,
  theme: 'cyber',
  setTheme: () => {},
  tc: THEMES.cyber,
  activeTab: 'hero',
  setActiveTab: () => {},
  selectedArtistName: '',
  setSelectedArtistName: () => {},
  selectedAlbumKey: '',
  setSelectedAlbumKey: () => {},
  selectedTrackKey: '',
  setSelectedTrackKey: () => {},
  topSubTab: 'artists',
  setTopSubTab: () => {},
});

export function AppProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const stored = localStorage.getItem('nml_lang');
    return isLang(stored) ? stored : 'es';
  });
  const [theme, setThemeState] = useState<Theme>(() => {
    const stored = localStorage.getItem('nml_theme');
    return isTheme(stored) ? stored : 'cyber';
  });

  // Global navigation & dossiers selection state
  const [activeTab, setActiveTab] = useState<string>('hero');
  const [selectedArtistName, setSelectedArtistName] = useState<string>('');
  const [selectedAlbumKey, setSelectedAlbumKey] = useState<string>('');
  const [selectedTrackKey, setSelectedTrackKey] = useState<string>('');
  const [topSubTab, setTopSubTab] = useState<'artists' | 'albums' | 'tracks'>('artists');

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    localStorage.setItem('nml_lang', l);
  }, []);

  const setTheme = useCallback((th: Theme) => {
    setThemeState(th);
    localStorage.setItem('nml_theme', th);
  }, []);

  // Apply CSS variables to :root whenever theme changes
  useEffect(() => {
    const tc = THEMES[theme];
    const root = document.documentElement;
    root.setAttribute('data-theme', theme);
    root.setAttribute('data-mode', tc.mode);
    root.style.setProperty('--c1', tc.c1);
    root.style.setProperty('--c2', tc.c2);
    root.style.setProperty('--c3', tc.c3);
    root.style.setProperty('--c4', tc.c4);
    root.style.setProperty('--bg', tc.bg);
    root.style.setProperty('--fg', tc.mode === 'light' ? '#1e293b' : '#f3f4f6');
    root.style.setProperty('--glass-bg', tc.mode === 'light' ? 'rgba(255, 255, 255, 0.55)' : 'rgba(10, 25, 47, 0.45)');
  }, [theme]);

  return (
    <AppContext.Provider value={{
      lang, setLang,
      t: STRINGS[lang] as TranslationKeys,
      theme, setTheme,
      tc: THEMES[theme],
      activeTab, setActiveTab,
      selectedArtistName, setSelectedArtistName,
      selectedAlbumKey, setSelectedAlbumKey,
      selectedTrackKey, setSelectedTrackKey,
      topSubTab, setTopSubTab,
    }}>
      {children}
    </AppContext.Provider>
  );
}

export function useApp() {
  return useContext(AppContext);
}
